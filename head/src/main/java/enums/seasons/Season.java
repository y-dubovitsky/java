package enums.seasons;

/**
 * Перечисление - это класс
 * Объявляя enum мы неявно создаем класс производный от java.lang.Enum.
 * Условно конструкция enum Season { ... } эквивалентна class Season extends java.lang.Enum { ... }.
 */
public enum Season {
    WINTER,
    SPRING,
    SUMMER,
    AUTUMN
}

